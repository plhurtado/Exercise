
public class Group {
		private String lastName;
		private int adults;
		private int kids;
		
		public Group (String lastName, int adults, int kids) {
			this.lastName = lastName;
			this.adults = adults;
			this.kids = kids;
		}
		
		public String getLastName() {
			return lastName;
		}
		public int getAdults() {
			return adults;
		}
		public int getKids() {
			return kids;
		}
		
}


